function generateId() {
  return Math.random().toString(36).substr(2, 9)
}

function generateObject({
  x,
  y,
  fill,
  width,
  height,
  text = '',
  draggable = true,
  type = 'rect',
  rotation = 0,
}) {
  return {
    id: generateId(),
    type,
    x,
    y,
    fill,
    width,
    height,
    draggable,
    text,
    rotation,
  }
}

function generateRoomObject({ x, y, fill, width, height }) {
  return generateObject({ x, y, fill, width, height, draggable: false })
}

